package application;

public class Person {
	private String name;
	private int age;
	
	public void setName(String text) {
		this.name = text;
	}
	
	public String getName() {
		return this.name;
	}
	
	public void setAge(int number) {
		this.age = number;
	}
	
	public int getAge() {
		return this.age;
	}

}
